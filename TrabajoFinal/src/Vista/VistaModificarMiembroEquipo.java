    public void llenarComboEquipo(){
     List<Equipo> listaEquipo= equiData.recuperarEquipos();
     if(listaEquipo.size()>0){
      for(Equipo e: listaEquipo){
        jcbEquipos.addItem(e);
      }
     }
     jcbEquipos.setSelectedIndex(-1);
    }
    
    public void llenarComboMiembro(){
     jdcFechaIncorporacion.setDate(null);
     jtfRol.setText(null);
     equipo= (Equipo) jcbEquipos.getSelectedItem();
     jcbMiembros.removeAllItems();
     List<Miembro> listaMiembros= equiMiembrosData.miembrosDelEquipo(equipo.getIdEquipo());
     if(listaMiembros.size()>0){
      for(Miembro m: listaMiembros){
       jcbMiembros.addItem(m);
      }
     }
     jcbMiembros.setSelectedIndex(-1);
    }
    
    public void limpiar(){
     jcbEquipos.removeAllItems();
     jcbMiembros.removeAllItems();
     jdcFechaIncorporacion.setDate(null);
     jtfRol.setText(null);
     jrbModificar.setSelected(false);
     jrbEliminar.setSelected(false);
    }
    
