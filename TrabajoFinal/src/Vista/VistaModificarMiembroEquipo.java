    private void jcbMiembrosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbMiembrosActionPerformed
        // TODO add your handling code here:
        if(jcbMiembros.getSelectedItem()!=null){
          jdcFechaIncorporacion.setDate(null);
          jtfRol.setText(null);
          jbGuardar.setEnabled(true);
          jdcFechaIncorporacion.setEnabled(false);
          jtfRol.setEnabled(true);
          miembro= (Miembro) jcbMiembros.getSelectedItem();
          em = equiMiembrosData.EquipoMiembro(equipo.getIdEquipo(), miembro.getIdMiembro()); 
          LocalDate ld= em.getFechaIncorporacion();
          Date date = (Date) Date.from(ld.atStartOfDay(ZoneId.systemDefault()).toInstant());
          jdcFechaIncorporacion.setDate(date); //.setDate(Date.valueOf(em.getFechaIncorporacion()));
          //jdcFechaIncorporacion.setDate(setDate);
          //String rol= em.getRol();
          jtfRol.setText(em.getRol());
          newRol= em.getRol(); //guardo el Rol en una variable String global
          
          if(jrbEliminar.isSelected()){
           jtfRol.setEnabled(false);
           jbEliminar.setEnabled(true);
           jbGuardar.setEnabled(false);
          }
          //JOptionPane.showMessageDialog(this, em.getFechaIncorporacion());
        }
        //jtfRol.setText("Nada de nada");
    }//GEN-LAST:event_jcbMiembrosActionPerformed

    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        // TODO add your handling code here:
        if(!newRol.equals(jtfRol.getText())){
          em.setEquipo((Equipo) jcbEquipos.getSelectedItem());
          em.setMiembro((Miembro) jcbMiembros.getSelectedItem());
          em.setRol(jtfRol.getText());
          equiMiembrosData.actualizarEquipoMiembro(em);
          JOptionPane.showMessageDialog(this, "Se ha modificado el rol del miembro en el equipo");
          limpiar();
        }
        else{
          jtfRol.requestFocus();
          JOptionPane.showMessageDialog(this, "Ingrese un nuevo Rol si desea modificarlo");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed

